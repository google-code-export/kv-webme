<h3 id="uploading-images">Uploading Images</h3>
<a href="#wrapper">back to top</a>
<p>In order to insert an image into a page, you must first upload it into your online files repository.</p>
<p>This can be done anywhere that you see a rich-text editor. For example, in Pages, open up any page and click either th Image icon, or the Link icon:</p>
<img src="upload-images/img1.png"/>
<p>After clicking this, a popup will appear. Click the "Browse Server" button.</p>
<p>This will open the File Manager window, where you can administrate your files repository.</p>

